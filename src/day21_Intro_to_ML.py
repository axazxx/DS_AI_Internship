#    Machine Learning consists of Three types of learning:
#        1 Supervised learning= In supervised learning the computer is given inputs aswell as their outputs
#          to learn and use them as training data.
#          ex: Spam Detection where email classification systems are trained on labeled data 
#          (e.g., "spam" vs. "not spam") to filter future emails.
#        2 Unsupervised learning= In Unsupervised No labels are given to the learning algorithm, leaving it on its own to find
#          the hidden patterns.
#          ex: Customer Segmentation where analyzing purchase behavior data to cluster customers into groups based on 
#          similarities such as high purchase and low purchase.
#        3 Reinforcement learning= the computer reaches its goals through trial and error.
#          ex: Gaming AI such as chess game which rewards on correct steps and penalizes on wrong steps.
