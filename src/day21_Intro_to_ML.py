# Machine Learning vs Traditional coding:
#    Machine learning is a subset of Artificial Intelligence. It is used to understand
#    patterns and learn from a given set of examples. In Traditional coding the coder gives 
#    instructions and rules for the code to execute whereas ML learns from the given input and
#    output and learns their patterns.
#    For example, if you want to detect spam emails, you might write rules like: “If the email
#    contains ‘free money,’ mark it as spam.” The computer simply follows the instructions you provide.
#    Whereas for ML, you give it thousands of emails labeled “spam” or “not spam,” and the algorithm
#    figures out which patterns best separate the two categories. The “rules” are not hand-written—they 
#    are learned from data.
